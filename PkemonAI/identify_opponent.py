# identify_opponent.py
# =======================================
# ニックネーム（OCR名など）を正式なポケモン名に変換するモジュール
# =======================================

NAME_MAP = {
    "りゅうくん": "ガブリアス",
    "つつみちゃん": "キラフロル",
    "バレル先生": "アラブルタケ",
}

def resolve_opponent_name(name: str) -> str:
    """
    OCRなどで読み取ったニックネームを正式なポケモン名に変換。
    未登録なら入力をそのまま返す。
    """
    return NAME_MAP.get(name, name)